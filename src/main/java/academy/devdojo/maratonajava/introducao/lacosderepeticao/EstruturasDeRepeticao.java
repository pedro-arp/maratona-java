package academy.devdojo.maratonajava.introducao.lacosderepeticao;

public class EstruturasDeRepeticao {
    // while; do while; for
    public static void main(String[] args) {
        int count = 20;
        while (count < 10) {
            System.out.println(count);
            count++;
        }


        do {
            System.out.println(count);

        } while (count < 10);



        for (int i = 0; i < 10; i++) {
            System.out.println(i);


        }

    }

}
